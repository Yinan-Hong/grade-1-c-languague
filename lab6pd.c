
#include <stdio.h>
#include <stdlib.h>


int main(int argc, char *argv[]) {
	float x, h, t;
	int n;
	scanf("%f %d",&h,&n);
	x=h;
	for (n>0;n--;){
		x=x+t*2;
		h=h/2;
		t=h;
		x=((int)(x*1000+5)/10) / 100.0;
		h=((int)(h*1000+5)/10) / 100.0;
		t=((int)(t*1000+5)/10) / 100.0;
	}
	printf("%.2f\n%.2f",x,t);
	return 0;
}
